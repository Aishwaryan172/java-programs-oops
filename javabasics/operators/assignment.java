package javabasics.operators;

public class assignment {
    public static void main(String[] args) {
        int a=25;
        System.out.println("sum= "+(a+=5));
        System.out.println("sum= "+(a-=5));
        System.out.println("sum= "+(a*=5));
        System.out.println("sum= "+(a/=5));
        System.out.println("sum= "+(a%=5));
    }   
}
